void UnitFunct::movUnit() {				  // переместить юнита
	char direct = command[4];	  // направление движения
	int steps = command[5] - '0'; // кол-во шагов
	int flagAttack = command[6] - '0';

	pointer->caseMovUnit(idGamer, x, y, direct, steps, flagAttack);
}

void UnitFunct::infoUnit() {
	pointer->infoUnit(x, y);
}

void UnitFunct::swapUnit() {
	int x2 = command[4] - '0';
	int y2 = command[5] - '0';
	pointer->swapUnits(x, y, x2, y2);
}

